
//Access Array Elements
//what is array 
const fruits = ["Banana", "Orange", "Apple", "Mango"];

let fruit = fruits[0];
console.log(fruit); // Output: "Banana"

console.log(fruits[2]); // Output: "Apple"

console.log(fruits[-1]); // Output: "Mango"
console.log(fruits[-2]); // Output: "Apple"

//Modify Array Elements

fruits[0] = "Kiwi";
console.log(fruits); 

// Output: ["Kiwi", "Orange", "Apple", "Mango"]    

